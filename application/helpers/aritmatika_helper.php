	function tambah($a,$b,$c)
	{
		$h = $a+$b+$c;
		return $h;
	}
	function kurang($a, $b, $c)
	{
		$h = $a-$b-$c;
		return $h;
	}
	function kali($a, $b, $c)
	{
		$h = $a*$b*$c;
		return $h;
	}
	function bagi($a, $b, $c)
	{
		$h = $a/$b/$c;
		return $h;
	}